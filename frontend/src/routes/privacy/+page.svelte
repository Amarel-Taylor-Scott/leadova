  <h1>Privacy Policy</h1>
  <p class="updated">Last updated: January 2026</p>

  <section>
    <h2>1. Overview</h2>
    <p>Leadova ("we", "us", "our") operates a consent-based lead generation platform. This Privacy Policy describes how we collect, use, store, and protect personal information submitted through our platform.</p>
    <p>We operate across multiple jurisdictions and comply with applicable data protection regulations including but not limited to:</p>
    <ul>
      <li><strong>Republic Act No. 10173</strong> (Data Privacy Act of 2012) -- Philippines</li>
      <li><strong>Telephone Consumer Protection Act (TCPA)</strong> -- United States</li>
      <li><strong>California Consumer Privacy Act (CCPA)</strong> -- California, United States</li>
      <li><strong>General Data Protection Regulation (GDPR)</strong> -- United Kingdom, European Union</li>
      <li><strong>Personal Data Protection Act (PDPA)</strong> -- Singapore</li>
      <li><strong>Australian Privacy Principles (APPs)</strong> -- Australia</li>
      <li><strong>Personal Information Protection and Electronic Documents Act (PIPEDA)</strong> -- Canada</li>
    </ul>
  </section>

  <section>
    <h2>2. Information We Collect</h2>
    <p>We collect personal information that you voluntarily provide when submitting a lead form, including but not limited to:</p>
    <ul>
      <li>Full name, email address, phone number</li>
      <li>Service-specific information (varies by vertical)</li>
      <li>Consent method, timestamp, and the exact text of the consent shown</li>
      <li>IP address and browser user agent (for verification and compliance)</li>
    </ul>
    <p>For subscribers and agents, we also collect account information including email, name, company name, and payment details (processed securely via Stripe).</p>
  </section>

  <section>
    <h2>3. Consent</h2>
    <p>We operate on a consent-first model. No personal data is collected without explicit consent. For each lead submission, we record:</p>
    <ul>
      <li>The consent method (web form, agent verbal consent, agent signature, or chatbot)</li>
      <li>The exact timestamp of consent</li>
      <li>The full text of the consent statement shown to the individual</li>
      <li>The IP address at time of consent (for web submissions)</li>
    </ul>
    <p>Under RA 10173, consent is defined as any freely given, specific, informed, and indicated willingness of the data subject to allow the processing of their personal information. Under TCPA, prior express written consent is obtained before any sharing with service providers.</p>
  </section>

  <section>
    <h2>4. How We Use Your Information</h2>
    <ul>
      <li>To connect you with verified service providers in your selected vertical</li>
      <li>To provide lead data to subscribed businesses for the purpose stated in the consent</li>
      <li>To verify lead quality and prevent fraud</li>
      <li>To comply with legal obligations and respond to data subject requests</li>
    </ul>
  </section>

  <section>
    <h2>5. Data Sharing</h2>
    <p>Your personal data is shared only with businesses who have active subscriptions to the specific vertical and country you submitted to. All subscribers agree to our Terms of Service which prohibit misuse of lead data.</p>
    <p>We do not sell personal data to third parties outside the scope of our stated lead generation services.</p>
  </section>

  <section>
    <h2>6. Your Rights</h2>
    <p>Depending on your jurisdiction, you have the right to:</p>
    <ul>
      <li><strong>Access</strong> your personal data</li>
      <li><strong>Correct</strong> inaccurate data</li>
      <li><strong>Delete</strong> your data (right to erasure / right to be forgotten)</li>
      <li><strong>Withdraw consent</strong> at any time</li>
      <li><strong>Data portability</strong> -- receive your data in a structured format</li>
      <li><strong>Object</strong> to processing of your data</li>
    </ul>
    <p>Under RA 10173, data subjects may exercise these rights by contacting the National Privacy Commission or our Data Protection Officer.</p>
    <p>Under CCPA, California residents have the right to know what personal information is collected, to delete it, and to opt out of its sale.</p>
    <p>To exercise any of these rights, contact us at <a href="mailto:privacy@leadova.com">privacy@leadova.com</a>.</p>
  </section>

  <section>
    <h2>7. Data Retention</h2>
    <p>Lead data is retained for up to 730 days (2 years) from the date of collection, unless consent is withdrawn earlier. When a lead withdraws consent, their data is soft-deleted immediately and permanently purged within 30 days.</p>
  </section>

  <section>
    <h2>8. Security</h2>
    <p>We implement industry-standard security measures including encrypted data transmission (TLS), encrypted storage, access controls, and regular security audits. All database access is logged for audit purposes.</p>
  </section>

  <section>
    <h2>9. Contact</h2>
    <p>For privacy inquiries, data requests, or to withdraw consent:</p>
    <p>Email: <a href="mailto:privacy@leadova.com">privacy@leadova.com</a></p>
  </section>
